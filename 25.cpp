#include <iostream>

int main() {
    // Введення числа K
    int K;
    std::cout << "Введіть число K (від 1 до 365): ";
    std::cin >> K;

    // Визначення номера дня тижня для K-го дня року
    int dayOfWeek = (K + 2) % 7; // 2 - це номер четверга (1 січня)

    // Виведення результату
    std::cout << "Для " << K << "-го дня року номер дня тижня: " << dayOfWeek << std::endl;

    return 0;
}
